const questionsLength = questions.length;
let quizIndex = 0;
let score = 0;

function questionShuffle(array) {
    for (let i = (array.length - 1); 0 < i; i--) {

        // 0〜(i+1)の範囲で値を取得
        let r = Math.floor(Math.random() * (i + 1));

        // 要素の並び替えを実行
        let tmp = array[i];
        array[i] = array[r];
        array[r] = tmp;
    }
    return array;
};
questionShuffle(questions);

const button = document.getElementsByTagName('button');
const buttonLength = button.length;

const setupQuiz = () => {
    document.getElementById('question').innerText = questions[quizIndex].poem;
    let buttonIndex = 0;
    const answerPos = Math.floor(Math.random() * 4);
    while (buttonIndex < buttonLength) {
        if (buttonIndex === answerPos) {
            button[buttonIndex].textContent = questions[quizIndex].volume;
        } else {
            let wrongVolume = questions[quizIndex].volume;
            while (wrongVolume === questions[quizIndex].volume) {
                wrongVolume = Math.floor(Math.random() * 74) + 1;
            }
            button[buttonIndex].textContent = wrongVolume;
        }
        buttonIndex++;
    }
}

setupQuiz();

const clickHandler = (e) => {
    if (String(questions[quizIndex].volume) === e.target.textContent) {
        window.alert("正解！");
        score++;
    } else {
        window.alert("不正解！\n正解は" + String(questions[quizIndex].volume) + "巻");
    }

    quizIndex++;

    if (quizIndex < questionsLength) {
        setupQuiz();
    } else {
        window.alert('終了！あなたの正解数は' + score + '/' + questionsLength + 'です！');
    }
}

let handlerIndex = 0;
while (handlerIndex < buttonLength) {
    button[handlerIndex].addEventListener('click', (e) => {
        clickHandler(e);
    });
    handlerIndex++;
}